import {
  createAvatarUrl,
  createPlayerId,
  type AuctionRecord,
  type PlayerNationality,
  type PlayerRole,
  type PlayerStats,
  type PlayerStatus,
  type RawPlayerSeed,
  type TeamId,
} from "./types";

const CURRENT_SEASON = 2026;
const PREVIOUS_SEASON = 2025;

type PlayerSeedConfig = {
  name: string;
  role: PlayerRole;
  nationality: PlayerNationality;
  price: string;
  previous?: {
    teamId: TeamId;
    price: string;
  };
  stats?: PlayerStats;
  isNew?: boolean;
  status?: PlayerStatus;
};

const defaultStats = (): PlayerStats => ({ matches: 0 });

const battingStats = (
  matches: number,
  runs: number,
  strikeRate: number,
  average?: number,
): PlayerStats => ({
  matches,
  runs,
  strikeRate,
  average,
});

const bowlingStats = (
  matches: number,
  wickets: number,
  economy: number,
): PlayerStats => ({
  matches,
  wickets,
  economy,
});

const allRoundStats = (
  matches: number,
  runs: number,
  wickets: number,
  strikeRate: number,
  economy: number,
  average?: number,
): PlayerStats => ({
  matches,
  runs,
  wickets,
  strikeRate,
  economy,
  average,
});

function createAuctionHistory(
  teamId: TeamId,
  price: string,
  previous?: PlayerSeedConfig["previous"],
): AuctionRecord[] {
  if (!previous) {
    return [{ year: CURRENT_SEASON, teamId, price }];
  }

  return [
    { year: PREVIOUS_SEASON, teamId: previous.teamId, price: previous.price },
    { year: CURRENT_SEASON, teamId, price },
  ];
}

function createPlayer(
  teamId: TeamId,
  fallbackStatus: PlayerStatus,
  config: PlayerSeedConfig,
): RawPlayerSeed {
  const id = createPlayerId(config.name);

  return {
    id,
    name: config.name,
    role: config.role,
    nationality: config.nationality,
    avatarUrl: createAvatarUrl(id),
    currentTeamId: teamId,
    status: config.status ?? fallbackStatus,
    isNew: config.isNew ?? false,
    season: CURRENT_SEASON,
    stats: config.stats ?? defaultStats(),
    auctionHistory: createAuctionHistory(teamId, config.price, config.previous),
  };
}

function createSquad(
  teamId: TeamId,
  playingXI: PlayerSeedConfig[],
  bench: PlayerSeedConfig[],
): RawPlayerSeed[] {
  return [
    ...playingXI.map((player) => createPlayer(teamId, "Playing XI", player)),
    ...bench.map((player) => createPlayer(teamId, "Bench", player)),
  ];
}

